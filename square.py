def area(a):
    ''' Find the area of square
            Parameters:
                a (double): len of sides
            Returned values:
                square_area (double): area of the square '''
    return round(a * a, 2)


def perimeter(a):
    ''' Find the perimeter of square
            Parameters:
                a (double): len of sides
            Returned values:
                square_per (double): perimeter of the square '''
    return round(4 * a, 2)
